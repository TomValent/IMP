void fillField(int fieldLength, char field[HEIGHT][MAX], char template[HEIGHT][WIDTH])
{
	for (int i=0; i < HEIGHT; i++) {
		for (int k = fieldLength; k < fieldLength+8; k++) {
			field[i][k] = template[i][k-fieldLength];
		}
	}
}

